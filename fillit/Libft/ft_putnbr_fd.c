#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char digit;

	digit = '\0';
	if (n == -2147483648)
	{
		write(fd, "-", 1);
		write(fd, "2", 1);
		n = 147483648;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		digit = n % 10 + '0';
		write(fd, &digit, 1);
	}
}
