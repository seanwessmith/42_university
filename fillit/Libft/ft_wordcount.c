#include "libft.h"

int		ft_wordcount(const char *s, char c)
{
	int i;

	i = 0;
	while (*s)
	{
		if (*s != c)
		{
			if (*(s + 1) == c || *(s + 1) == '\0')
			{
				i++;
			}
		}
		s++;
	}
	return (i);
}
