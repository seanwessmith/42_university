#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *value;

	value = 0;
	while (*s != '\0')
	{
		if (*s == (char)c)
		{
			value = (char*)s;
		}
		s++;
	}
	if (c == '\0')
	{
		return (char*)s;
	}
	return (value);
}
