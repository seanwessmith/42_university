#include "libft.h"

char	*ft_chrcat(char *s1, const char c)
{
	int		i;

	i = 0;
	if ((!c) || !s1)
		return (s1);
	while (s1[i] != '\0')
		i++;
	s1[i] = c;
	s1[++i] = '\0';
	return (s1);
}
